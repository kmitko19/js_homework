// Задание 2
// Функция преобразования числа в объект: единицы, десятки, сотни
//----------------------------------------------------------------------------------
function isNumeric(n) {																		// Функция проверки значения на число
  return !isNaN(parseFloat(n)) && isFinite(n);						// Сначала отсекаем "true/false/null/''", затем "Infinity/-Infinity"
}

var num = {'Единицы' : '', 'Десятки' : '', 'Сотни' : ''};	// Создаем пустой объект

function numToObj (num) {

	var n;

	n = prompt ('Введите число в диапазоне от 0 до 999:');	// Запрашиваем ввод числа

	if (!isNumeric(n)) {																		// Проверка введенного значения
		alert ('Введенное значение не является числом');			// Если не число, то выполнение прерывается
		return;					
	} else if (n < 0) {																			// Если число не в интревале от 0 до 999, то прекращаем работу
		alert ('Введенное число меньше 0');
		return;					
	}	else if (n > 999) {
		alert ('Введенное число больше 999');
		return;					
	}

	if (n.length == 2) {																		// Если число двузначное, то слева добавляем "0"	
		n = '0' + n;	
	} else if (n.length == 1) {															// Если число однозначное, то слева добавляем "00"
		n = '00' + n;
	}

	var i = 2;	
	for (var key in num) {																	// Перебираем свойства объекта и присваиваем им значения		
		num[key] = +n[i];
		i--;			
	}				
	return num;
}

console.log (numToObj(num));															// Вывод результатов на консоль

