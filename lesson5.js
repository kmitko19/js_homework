var str = '';

function objectToQueryString (object) {
	var i = 0;															// Описание счетчика и создание	
	for (var key in object) {								// проходим по свойствам объекта
		if (i > 0) {													// Пропускаем при первом входе присвоение "&"
			str = str + '&';
		}		
		str = str + key + '=' + object[key];	// Формируем строку из названия свойства и значения			
		i++;
	}
	return str;
}

